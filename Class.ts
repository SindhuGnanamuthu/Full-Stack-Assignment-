class Person {
  name: string;
  age: number;

  constructor(name: string, age: number) {
    this.name = name;
    this.age = age;
  }

  greet(): void {
    console.log(`Hello, my name is ${this.name}`);
  }
}
const person1 = new Person("Alice", 30);
person1.greet(); // Output: Hello, my name is Alice
